// Challenge:
// Predict whether true or false is logged to the console

console.log(4 === 3);
console.log(5 > 2);
console.log(12 > 12);
console.log(3 < 0);
console.log(3 >= 3);
console.log(11 <= 11);
console.log(3 <= 2);


// Solution:

console.log(4 === 3); // false
console.log(5 > 2); // true
console.log(12 > 12); // false
console.log(3 < 0); // false
console.log(3 >= 3); // true
console.log(11 <= 11); // true
console.log(3 <= 2); // false