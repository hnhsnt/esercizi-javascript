// Challenge
// 1. Create two functions, add3Points() and remove1Point(), and have them
// add/remove points to/from the myPoints variable
// 2. Call the functions so that the line below logs out 10

// start with this
let myPoints = 3;


// Solution:
// 1.
function add3Points() {
    myPoints += 3;
}

function remove1Point() {
    myPoints -= 1;
}

// 2.
add3Points();
add3Points();
add3Points();
remove1Point();
remove1Point();


