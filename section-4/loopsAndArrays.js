let largeCountries = ["China", "India", "USA", "Indonesia", "Pakistan"];

// Challenge:
/* Use a for loop to log the following to the console

The 5 largest countries in the world:
- China
- India
- United States
- Indonesia
- Pakistan
*/


// Solution:

